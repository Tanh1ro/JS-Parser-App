// Basic Expressions
const x = 10;
const y = 20;

// Arithmetic operations
const sum = x + y;
const difference = x - y;
const product = x * y;
const quotient = x / y;

// More complex expressions
const expression1 = (x + y) * 2;
const expression2 = x ** 2 + y ** 2;

// String operations
const greeting = "Hello";
const name = "World";
const message = greeting + ", " + name + "!";
const template = `${greeting}, ${name}!`;

// Output results
sum;          // 30
difference;   // -10
product;      // 200
quotient;     // 0.5
expression1;  // 60
expression2;  // 500
message;      // "Hello, World!"
template;     // "Hello, World!" 